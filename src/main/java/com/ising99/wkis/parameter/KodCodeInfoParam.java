public class KodCodeInfoParam {

    private int id;
    private String mac;
    private String sncode;
    private String koiccode;
    private String randusercode;
    private int stated;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getSncode() {
        return sncode;
    }

    public void setSncode(String sncode) {
        this.sncode = sncode;
    }

    public String getKoiccode() {
        return koiccode;
    }

    public void setKoiccode(String koiccode) {
        this.koiccode = koiccode;
    }

    public String getRandusercode() {
        return randusercode;
    }

    public void setRandusercode(String randusercode) {
        this.randusercode = randusercode;
    }

    public int getStated() {
        return stated;
    }

    public void setStated(int stated) {
        this.stated = stated;
    }
}
